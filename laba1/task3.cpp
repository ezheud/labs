// Лабораторная работа №1, задание 3
#include <iostream>
using namespace std;

// Прототипы функций обмена
void obmen1(int, int);      // Передача по значению (НЕ работает)
void obmen2(int*, int*);    // Передача по указателю (работает)
void obmen3(int&, int&);    // Передача по ссылке (работает)

int main() {
    int a = 2, b = 5;
    
    cout << "--------" << endl;
    cout << "Исходные значения: a = " << a << ", b = " << b << endl;
    cout << "--------" << endl << endl;
    
    // 1. Передача по значению
    cout << "1. Вызов obmen1 (передача по значению):" << endl;
    cout << "   До обмена: a = " << a << ", b = " << b << endl;
    obmen1(a, b);
    cout << "   После обмена: a = " << a << ", b = " << b << " (без изменений!)" << endl;
    cout << "   Вывод: значения НЕ изменились, так как функция работала с копиями" << endl << endl;
    
    // 2. Передача по указателю
    cout << "2. Вызов obmen2 (передача по указателю):" << endl;
    cout << "   До обмена: a = " << a << ", b = " << b << endl;
    obmen2(&a, &b);  // Передаем адреса переменных
    cout << "   После обмена: a = " << a << ", b = " << b << " (значения изменились!)" << endl;
    cout << "   Вывод: значения изменились, так как функция работала с адресами" << endl << endl;
    
    // Восстанавливаем исходные значения для третьего эксперимента
    a = 2;
    b = 5;
    cout << "Восстановлены исходные значения для третьего эксперимента:" << endl;
    cout << "a = " << a << ", b = " << b << endl << endl;
    
    // 3. Передача по ссылке
    cout << "3. Вызов obmen3 (передача по ссылке):" << endl;
    cout << "   До обмена: a = " << a << ", b = " << b << endl;
    obmen3(a, b);
    cout << "   После обмена: a = " << a << ", b = " << b << " (значения изменились!)" << endl;
    cout << "   Вывод: значения изменились, так как ссылки являются псевдонимами переменных" << endl << endl;
    
    cout << "------" << endl;
    cout << "Выводы:" << endl;
    cout << "- obmen1: передача по значению - создаются копии, оригиналы не меняются" << endl;
    cout << "- obmen2: передача по указателю - работа с адресами, оригиналы меняются" << endl;
    cout << "- obmen3: передача по ссылке - работа с псевдонимами, оригиналы меняются" << endl;
    cout << "----" << endl;
    
    return 0;
}

// Вариант 1: Передача параметров по значению
// Функция получает копии значений a и b
// Изменения внутри функции НЕ влияют на исходные переменные
void obmen1(int x, int y) {
    cout << "   Внутри obmen1 (до обмена): x = " << x << ", y = " << y << endl;
    int temp = x;
    x = y;
    y = temp;
    cout << "   Внутри obmen1 (после обмена): x = " << x << ", y = " << y << endl;
}

// Вариант 2: Передача параметров по указателю
// Функция получает адреса переменных
// Изменения по этим адресам влияют на исходные переменные
void obmen2(int *x, int *y) {
    cout << "   Внутри obmen2 (до обмена): *x = " << *x << ", *y = " << *y << endl;
    int temp = *x;  // Разыменовываем указатели для доступа к значениям
    *x = *y;
    *y = temp;
    cout << "   Внутри obmen2 (после обмена): *x = " << *x << ", *y = " << *y << endl;
}

// Вариант 3: Передача параметров по ссылке
// Функция получает ссылки (псевдонимы) на переменные
// Изменения напрямую влияют на исходные переменные
void obmen3(int &x, int &y) {
    cout << "   Внутри obmen3 (до обмена): x = " << x << ", y = " << y << endl;
    int temp = x;
    x = y;
    y = temp;
    cout << "   Внутри obmen3 (после обмена): x = " << x << ", y = " << y << endl;
}